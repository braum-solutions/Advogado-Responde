package com.braumsolutions.advogadoresponde.Model;

public class CasesModel {

    String description, occupation_area, pdf, picture, user;

    public CasesModel() {

    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getOccupation_area() {
        return occupation_area;
    }

    public void setOccupation_area(String occupation_area) {
        this.occupation_area = occupation_area;
    }

    public String getPdf() {
        return pdf;
    }

    public void setPdf(String pdf) {
        this.pdf = pdf;
    }

    public String getPicture() {
        return picture;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }
}
